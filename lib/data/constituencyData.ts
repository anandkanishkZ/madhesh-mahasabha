// Get constituencies by district
export function getConstituenciesByDistrict(district: string): string[] {
  // Clean and normalize the district name
  const cleanedDistrict = district.trim().toLowerCase();
  
  // Special mappings for complex district names
  const districtMappings: Record<string, string> = {
    'eastern rukum': 'Eastern Rukum',
    'eastern-rukum': 'Eastern Rukum',
    'western rukum': 'Western Rukum',
    'western-rukum': 'Western Rukum',
    'nawalpur': 'Nawalpur',
    'parasi': 'Nawalparasi (West of Bardaghat Susta)',
    'nawalparasi west': 'Nawalparasi (West of Bardaghat Susta)',
  };
  
  // Check if there's a special mapping
  if (districtMappings[cleanedDistrict]) {
    return constituencyData[districtMappings[cleanedDistrict]] || [];
  }
  
  // Capitalize first letter to match the constituency data keys
  const capitalizedDistrict = cleanedDistrict.charAt(0).toUpperCase() + cleanedDistrict.slice(1);
  
  // Try different variations
  return constituencyData[capitalizedDistrict] || 
         constituencyData[district] || 
         constituencyData[district.trim()] || 
         [];
}

